export function getHookBySlug(slug: string): ContentHook | undefined {
  return contentHooks.find((h) => h.slug === slug);
}

export function getHooksByType(type: ContentHook['type']): ContentHook[] {
  return contentHooks.filter((h) => h.type === type);
}

export function getHooksByPlacement(placement: ContentHook['placement']): ContentHook[] {
  return contentHooks.filter((h) => h.placement === placement);
}

export function getReadyHooks(): ContentHook[] {
  return contentHooks.filter((h) => h.status === 'ready');
}
